#include<bits/stdc++.h>
using namespace std;
int main()
{
    int x=10;
    int y=10;
    x++;
    cout<<"10 Post increment value: "<<x<<endl;
    ++y;
    cout<<"10 Pre increment value: "<<y<<endl;
    y--;
    cout<<"Post decrement value: "<<y<<endl;
    --x;
    cout<<"Pre decrement value: "<<x<<endl;
}